const data = new Array(4096).fill(0x0);

const NOP = [0xDF, 0x80, 0x0F, 0x80, 40];

// INSTRUCTION 0X0
const NOOP = [
    // [0xdf, 0x80, 0x0f, 0x80 ,0x40],
    // [0xf0, 0x00, 0x0f, 0x80, 0x00],
    // [0x7f, 0x00, 0x0F, 0x80, 0x40]
    [0x80, 0x80, 0x80, 0x80, 0x80],
    [0x40, 0x40, 0x40, 0x40, 0x40],
    [0x20, 0x20, 0x20, 0x20, 0x20],
    [0x10, 0x10, 0x10, 0x10, 0x10]
]

const insertInstruction = (inst, address) => {
    for (let a = address; a < inst.length; a++) {
        data[a] = inst[a];
    }
}

insertInstruction(NOOP, 0x0);

export default data;